        String jobDepartmentName=department_name.getText();
        String jobDepartureDate=departure_date.getText();
        Connection conn=OracleConnection.oracleConnect();
            try{
            String query="INSERT INTO JOB_ALLOCATION(id, emp_code, event_name,"+
           "branch_name, department_name, department_date) VALUES(?,?,?,?,?,?)";
            ps=conn.prepareStatement(query);
            ps.setString(1, jobID);
            ps.setString(2, jobEmpCode);
            ps.setString(3, jobEvent);
            ps.setString(4, jobBranch);
            ps.setString(5, jobDepartmentName);
            ps.setString(6, jobDepartureDate);
            ps.execute();
            JOptionPane.showMessageDialog(null, "Data Inserted");
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
