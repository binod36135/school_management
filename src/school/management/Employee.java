    private void empInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empInsertActionPerformed
        // TODO add your handling code here:
       
        
        String empCode=emp_code.getText();
        String empName=name.getText();
        String empDesignation=degination.getText();
        String empAddress=address.getText();
        String empDate=joined_date.getText();
        
        
        Connection conn=OracleConnection.oracleConnect();
        
        try{
          
           
           String query="INSERT INTO employee(emp_code, name,designation,address,joined_date)"+ "VALUES(?, ?, ?, ?, ?)";
           
          ps=conn.prepareStatement(query);
          ps.setString(1, empCode);
          ps.setString(2, empName);
          ps.setString(3, empDesignation);
          ps.setString(4, empAddress);
          ps.setString(5, empDate);
          ps.execute();
           JOptionPane.showMessageDialog(null, "data inserted");
           conn.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
 
    }//GEN-LAST:event_empInsertActionPerformed

